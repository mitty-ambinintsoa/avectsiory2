                                <p>Au-delà de l'installation, le projet s'est appuyé sur une campagne de sensibilisation menée par les bénévoles de GHM auprès des jeunes et des familles du quartier, afin de favoriser une appropriation durable des pratiques de tri. Les déchets collectés sont ensuite transmis à une entreprise spécialisée dans le recyclage, dans une démarche de valorisation et de réduction de l'impact environnemental.</p>
