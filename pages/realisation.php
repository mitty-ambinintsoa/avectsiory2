                                    <li>Analyse genre du cadre législatif et réglementaire existant dans le secteur de l’énergie, afin d’en identifier les forces, les lacunes et les leviers à amélioration.</li>
                                    <li>Élaboration de recommandations stratégiques concrètes, visant à corriger les inégalités, prévenir les discriminations et promouvoir l’accès équitable aux ressources et aux opportunités énergétiques, notamment pour les femmes et les groupes marginalisés.</li>
