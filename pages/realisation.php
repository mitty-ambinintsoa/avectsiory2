                                <p>Dans le cadre de son engagement en faveur de la justice sociale, de l’autonomisation des femmes et de la transition écologique inclusive, Green for a Healthy Madagascar a activement pris part à l’atelier de revue du cadre légal et réglementaire du secteur de l’énergie à Madagascar, organisé en partenariat avec le <strong>PNUD</strong>, le <strong>Ministère de l’Énergie</strong>, ainsi que le <strong>Ministère de la Population et de la Solidarité</strong>.</p>
                                <p>Cette initiative stratégique s’inscrit dans le processus de formulation d’une politique énergétique sensible au genre, en cohérence avec la Politique Nationale de l’Égalité Femme-Homme actuellement en cours de finalisation.</p>
